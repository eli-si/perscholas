package org.perscholas.control_flows_and_loops;
import java.util.Scanner;

public class Case_4 {
    public static void main(String[] args) {
        int x = 15;
        if (x < 10 || x > 20) {
            System.out.println("Out of range");
        } else {
            System.out.println("In range");
        }

        x = 5;
        if (x < 10 || x > 20) {
            System.out.println("Out of range");
        } else {
            System.out.println("In range");
        }
    }
}
